public class ProdutoVendaGeral {

    private int idLista, idVendaGeral, qnt, idProduto;

    public ProdutoVendaGeral() {
    }
    
    public int getIdLista() {
        return idLista;
    }

    public void setIdLista(int idLista) {
        this.idLista = idLista;
    }

    public int getIdVendaGeral() {
        return idVendaGeral;
    }

    public void setIdVendaGeral(int idVendaGeral) {
        this.idVendaGeral = idVendaGeral;
    }

    public int getQnt() {
        return qnt;
    }

    public void setQnt(int qnt) {
        this.qnt = qnt;
    }

    public int getIdProduto() {
        return idProduto;
    }

    public void setIdProduto(int idProduto) {
        this.idProduto = idProduto;
    }

}
